#include <bits/stdc++.h>
using namespace std;
typedef long long ll;

void solve(long long H, long long W, std::vector<std::vector<long long>> A)
{

    ll m = 1e+9;
    for (ll i = 0; i < H; i++)
    {
        for (ll j = 0; j < W; j++)
        {
            m = min(m, A[i][j]);
        }
    }
    ll ans = 0;
    for (ll i = 0; i < H; i++)
    {
        for (ll j = 0; j < W; j++)
        {
            ans += A[i][j] - m;
        }
    }
    cout << ans << endl;
}

signed main()
{
    long long H;
    scanf("%lld", &H);
    long long W;
    scanf("%lld", &W);
    std::vector<std::vector<long long>> A(H, std::vector<long long>(W));
    for (int i = 0; i < H; i++)
    {
        for (int j = 0; j < W; j++)
        {
            scanf("%lld", &A[i][j]);
        }
    }
    solve(H, W, std::move(A));
    return 0;
}
