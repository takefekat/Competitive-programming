#include <bits/stdc++.h>
using namespace std;
typedef long long ll;

void solve(long long N, std::vector<long long> A)
{
    sort(A.begin(), A.end());

    vector<ll> sum(N);
    sum[N - 1] = A[N - 1];
    for (ll i = N - 2; i >= 0; i--)
    {
        sum[i] = sum[i + 1] + A[i];
    }

    ll ans = 0;
    for (ll i = 0; i < N - 1; i++)
    {
        ans += sum[i + 1] - A[i] * (N - i - 1);
    }
    cout << ans << endl;
}

signed main()
{
    long long N;
    scanf("%lld", &N);
    std::vector<long long> A(N);
    for (int i = 0; i < N; i++)
    {
        scanf("%lld", &A[i]);
    }
    solve(N, std::move(A));
    return 0;
}
